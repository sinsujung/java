package com.test.java;

public class Ex06_Variable {
	public static void main(String[] args) {
		
		//Ex06_Variable.java
		
		//목표 > 자료형 + 변수 + 리터럴
		
		//1. 주변의 데이터 검색! > 나의 몸무게 > 몸무게를 저장할 변수 생성
		//2. 형태 + 길이 > 자료형 결정 > double
		//3. 변수 생성 + 값 대입
		//4. 출력
		//각 자료형 (값형 8가지, 문자열) x 10회 = 9 x 10회
		
		//1회
		double myWeight;
		myWeight = 70.5;
		System.out.println("제 몸무게는" + myWeight + "kg입니다.");
		
		//2회
		byte student;
		student = 30;
		System.out.println("반 학생 수는" + student + "입니다.");
		
		//3회
		byte team;
		team = 8;
		System.out.println("경기를 진행할 팀의 수는 " + team + "입니다.");
		
		//4회
		byte comicBook;
		comicBook = 7;
		System.out.println("집에 있는 만화책의 수는 " + comicBook + "입니다.");
		
		//5회
		byte family;
		family = 5 ;
		System.out.println("우리 집 가족의 수는 " + family + "명 입니다.");
		
		//6회
		byte car;
		car = 21 ;
		System.out.println("주차장에 있는 차의 수는 " + car + "입니다.");
		
		//7회
		byte desk;
		desk = 30;
		System.out.println("교실 안에 있는 책상의 수는 " + desk + "입니다.");
		
		//8회
		byte classRoom;
		classRoom = 5;
		System.out.println("1학년의 학급 수는 " + classRoom + "입니다.");
		
		//9회
		byte bread;
		bread = 3;
		System.out.println("매일 들어오는 포켓몬 빵의 개수는 " + bread + "입니다.");
		
		//10회
		double kyonggiPeople;
		kyonggiPeople = 1358.9;
		System.out.println("경기도 인구 수는 " + kyonggiPeople + "명 입니다.");
		
		//11회
		float rainfall;
		rainfall = 0.8F;
		System.out.println("현재 강수량은 " + rainfall + "입니다.");
		
		//12회
		double height;
		height = 162.3;
		System.out.println("나의 키는 " + height + "cm 입니다.");
		
		//13회
		
	}
}
